package mahavijay;
public class AddNumbers {
    public static void main(String[] args) {
        // Define two numbers
        int number1 = 5;
        int number2 = 10;

        // Add the numbers
        int sum = number1 + number2;

        // Print the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}
