package naresh;

class MaximumFind{

	void max(int num1, int num2){
		if (num1>num2)
		System.out.println("17 is Greater between 10 and 17.");
	}
	
	void min(int num1, int num2){
		if (num1<num2)
		System.out.println("10 is smaller between 10 and 17.");
	}

	public static void main(String[] args){
		MaximumFind max2=new MaximumFind();
		max2.max(17,10);
		max2.min(10,17);
}
}

	








	